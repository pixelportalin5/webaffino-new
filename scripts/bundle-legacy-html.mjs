import crypto from "node:crypto";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { PAGE_DEFINITIONS } from "../lib/legacyPageManifest.js";

const rootDir = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");
const outputPath = path.join(rootDir, "lib/legacyPageSources.js");
const hashPath = path.join(rootDir, "lib/.legacy-bundle-hash");
const sources = {};

const sourceHash = crypto.createHash("sha256");

for (const [key, definition] of Object.entries(PAGE_DEFINITIONS)) {
  const sourcePath = path.join(rootDir, definition.sourceFile);
  const sourceContents = fs.readFileSync(sourcePath, "utf8");
  sourceHash.update(key);
  sourceHash.update(sourceContents);
  sources[key] = sourceContents;
}

const currentHash = sourceHash.digest("hex");
let previousHash = "";

try {
  previousHash = fs.readFileSync(hashPath, "utf8").trim();
} catch {
  previousHash = "";
}

if (
  previousHash === currentHash &&
  fs.existsSync(outputPath) &&
  fs.statSync(outputPath).size > 0
) {
  console.log(
    `Legacy bundle up to date (${Object.keys(sources).length} pages), skipping write`
  );
  process.exit(0);
}

const fileContents = `/** Auto-generated by scripts/bundle-legacy-html.mjs — do not edit manually */\n\nexport const LEGACY_PAGE_SOURCES = ${JSON.stringify(sources)};\n`;

const tempPath = `${outputPath}.tmp`;
fs.writeFileSync(tempPath, fileContents, "utf8");
fs.renameSync(tempPath, outputPath);
fs.writeFileSync(hashPath, `${currentHash}\n`, "utf8");

console.log(`Bundled ${Object.keys(sources).length} legacy pages into lib/legacyPageSources.js`);
